package com.javalearning;

public class Main {

    public static void main(String[] args) {

        char mySimpleChar = 'D';
        char myUnicodeChar = '\u0044'; //unicode conventions

        System.out.println("SIMPLE CHAR = " + mySimpleChar);
        System.out.println("UNICODE CHAR = " + myUnicodeChar);

        boolean trueValue = true;
        boolean falseValue = false;

        System.out.println("TRUE VALUE = " + trueValue);
        System.out.println("FALSE VALUE = " + falseValue);

    }
}
